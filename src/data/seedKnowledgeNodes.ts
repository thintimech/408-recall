import type { KnowledgeNode } from '@/types/domain'

const seedTimestamp = '2026-05-19T00:00:00.000Z'

function subject(id: string, title: string, sortOrder: number): KnowledgeNode {
  return {
    id,
    subjectId: id,
    parentId: null,
    title,
    level: 0,
    sortOrder,
    description: '',
    createdAt: seedTimestamp,
    updatedAt: seedTimestamp
  }
}

function chapter(
  id: string,
  subjectId: string,
  title: string,
  sortOrder: number
): KnowledgeNode {
  return {
    id,
    subjectId,
    parentId: subjectId,
    title,
    level: 1,
    sortOrder,
    description: '',
    createdAt: seedTimestamp,
    updatedAt: seedTimestamp
  }
}

function section(
  id: string,
  subjectId: string,
  parentId: string,
  title: string,
  sortOrder: number
): KnowledgeNode {
  return {
    id,
    subjectId,
    parentId,
    title,
    level: 2,
    sortOrder,
    description: '',
    createdAt: seedTimestamp,
    updatedAt: seedTimestamp
  }
}

